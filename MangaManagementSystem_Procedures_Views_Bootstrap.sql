            N'manga_chapter_page_task_return_rework_' + CONVERT(NVARCHAR(36), @chapter_page_task_id);

        DECLARE @lock_result INT;

        EXEC @lock_result = sys.sp_getapplock
            @Resource = @lock_resource,
            @LockMode = 'Exclusive',
            @LockOwner = 'Transaction',
            @LockTimeout = 10000;

        IF @lock_result < 0
        BEGIN
