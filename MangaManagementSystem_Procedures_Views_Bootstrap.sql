CREATE OR ALTER PROCEDURE audit.usp_AuditEvent_Append
    @actor_user_id      UNIQUEIDENTIFIER = NULL,
    @action_code        NVARCHAR(64),
    @entity_type        NVARCHAR(128),
    @entity_id          NVARCHAR(100) = NULL,
    @detail_json        NVARCHAR(MAX) = NULL
AS
BEGIN
    SET NOCOUNT ON;
    SET XACT_ABORT ON;

    DECLARE @started_tran BIT = 0;
    DECLARE @lock_result INT;
    DECLARE @actor_role_name NVARCHAR(128) = NULL;

    BEGIN TRY
        IF @detail_json IS NOT NULL AND ISJSON(@detail_json) <> 1
        BEGIN
            ;THROW 50001, 'detail_json must be valid JSON.', 1;
        END;

        IF @@TRANCOUNT = 0
        BEGIN
            SET @started_tran = 1;
            BEGIN TRAN;
        END;

        /*
            Resolve actor role snapshot inside the audit procedure.
            This prevents the backend from passing role-name text manually.
        */
        IF @actor_user_id IS NOT NULL
        BEGIN
            SELECT
                @actor_role_name = r.role_name
            FROM
            (
                SELECT
                    u.user_id,
                    u.role_id
                FROM auth.Users u
                WHERE u.user_id = @actor_user_id
            ) AS actor_user
            INNER JOIN auth.Roles r
                ON r.role_id = actor_user.role_id;

            IF @actor_role_name IS NULL
            BEGIN
                ;THROW 50003, 'Actor user role could not be resolved.', 1;
            END;
        END;

        EXEC @lock_result = sys.sp_getapplock
            @Resource = N'audit_event_append',
            @LockMode = 'Exclusive',
            @LockOwner = 'Transaction',
            @LockTimeout = 10000;

        IF @lock_result < 0
        BEGIN
            ;THROW 50002, 'Could not acquire audit event append lock.', 1;
        END;

        INSERT INTO audit.AuditEvent
        (
            actor_user_id,
            actor_role_name,
            action_code,
            entity_type,
            entity_id,
            detail_json
        )
        VALUES
        (
            @actor_user_id,
            @actor_role_name,
            @action_code,
            @entity_type,
            @entity_id,
            @detail_json
        );

        IF @started_tran = 1
        BEGIN
            COMMIT;
        END;
    END TRY
    BEGIN CATCH
        IF @started_tran = 1 AND XACT_STATE() <> 0
        BEGIN
            ROLLBACK;
        END;

        ;THROW;
    END CATCH
END;
GO
CREATE OR ALTER PROCEDURE auth.usp_User_Create
    @role_name               NVARCHAR(30),
    @username                NVARCHAR(50),
    @email                   NVARCHAR(254),
    @password_hash           NVARCHAR(255),
    @display_name            NVARCHAR(100) = NULL,
    @avatar_file_id          UNIQUEIDENTIFIER = NULL,
    @portfolio_file_id       UNIQUEIDENTIFIER = NULL,

    @created_by_user_id      UNIQUEIDENTIFIER = NULL,

    @new_user_id             UNIQUEIDENTIFIER OUTPUT
AS
BEGIN
    SET NOCOUNT ON;
    SET XACT_ABORT ON;

    DECLARE @started_tran BIT = 0;
    DECLARE @detail_json NVARCHAR(MAX);

    DECLARE @resolved_role_id UNIQUEIDENTIFIER;
    DECLARE @resolved_role_name NVARCHAR(30);

    DECLARE @audit_actor_user_id UNIQUEIDENTIFIER;
    DECLARE @audit_entity_id NVARCHAR(100);

    DECLARE @normalized_role_name NVARCHAR(30) = LTRIM(RTRIM(@role_name));
    DECLARE @normalized_username NVARCHAR(50) = LTRIM(RTRIM(@username));
    DECLARE @normalized_email NVARCHAR(254) = LOWER(LTRIM(RTRIM(@email)));
    DECLARE @normalized_display_name NVARCHAR(100) = NULLIF(LTRIM(RTRIM(@display_name)), N'');

    DECLARE @created_user TABLE
    (
        user_id     UNIQUEIDENTIFIER NOT NULL,
        status_code NVARCHAR(30) NOT NULL
    );

    BEGIN TRY
        IF @normalized_display_name IS NULL
        BEGIN
            SET @normalized_display_name = @normalized_username;
        END;

        IF @@TRANCOUNT = 0
        BEGIN
            SET @started_tran = 1;
            BEGIN TRAN;
        END;

        /*
            Resolve requested role name into role_id.
            App passes role name; database stores GUID role_id.
        */
        SELECT
            @resolved_role_id = r.role_id,
            @resolved_role_name = r.role_name
        FROM auth.Roles r
        WHERE UPPER(LTRIM(RTRIM(r.role_name))) = UPPER(@normalized_role_name);

        IF @resolved_role_id IS NULL
        BEGIN
            ;THROW 50015, 'Invalid role name.', 1;
        END;

        /*
            auth.Users.user_id is generated by DEFAULT NEWID().
            Capture the generated GUID using OUTPUT inserted.user_id.
        */
        INSERT INTO auth.Users
        (
            role_id,
            username,
            email,
            password_hash,
            display_name,
            avatar_file_id,
            portfolio_file_id,
            status_code
        )
        OUTPUT
            inserted.user_id,
            inserted.status_code
        INTO @created_user
        (
            user_id,
            status_code
        )
        VALUES
        (
            @resolved_role_id,
            @normalized_username,
            @normalized_email,
            @password_hash,
            @normalized_display_name,
            @avatar_file_id,
            @portfolio_file_id,
            N'PENDING_APPROVAL'
        );

        SELECT
            @new_user_id = user_id
        FROM @created_user;

        /*
            If created by Admin/staff, audit actor is the creator.
            If self-registration, audit actor is the newly created user.
            audit.usp_AuditEvent_Append resolves actor_role_name internally.
        */
        SET @audit_actor_user_id = COALESCE(@created_by_user_id, @new_user_id);
        SET @audit_entity_id = CONVERT(NVARCHAR(36), @new_user_id);

        SELECT @detail_json =
        (
            SELECT
                @new_user_id AS user_id,
                @resolved_role_id AS role_id,
                @resolved_role_name AS role_name,
                @normalized_username AS username,
                @normalized_email AS email,
                @normalized_display_name AS display_name,
                N'PENDING_APPROVAL' AS status_code
            FOR JSON PATH, WITHOUT_ARRAY_WRAPPER
        );

        EXEC audit.usp_AuditEvent_Append
    @actor_user_id = @audit_actor_user_id,
    @action_code = N'USER_REGISTERED',
    @entity_type = N'Users',
    @entity_id = @audit_entity_id,
    @detail_json = @detail_json;

        IF @started_tran = 1
        BEGIN
            COMMIT;
        END;
    END TRY
    BEGIN CATCH
        DECLARE @error_number INT = ERROR_NUMBER();

        IF @started_tran = 1 AND XACT_STATE() <> 0
        BEGIN
            ROLLBACK;
        END;

        IF @error_number IN (2601, 2627)
        BEGIN
            ;THROW 50011, 'Username or email already exists.', 1;
        END;

        ;THROW;
    END CATCH
END;
GO
CREATE OR ALTER PROCEDURE auth.usp_Admin_ChangeUserStatus
    @admin_user_id              UNIQUEIDENTIFIER,
    @target_user_id             UNIQUEIDENTIFIER,
    @new_status_code            NVARCHAR(30),
    @reason                     NVARCHAR(500) = NULL
AS
BEGIN
    SET NOCOUNT ON;
    SET XACT_ABORT ON;

    DECLARE @started_tran BIT = 0;
    DECLARE @lock_result INT;
    DECLARE @lock_resource NVARCHAR(255);

    DECLARE @old_status_code NVARCHAR(30);
    DECLARE @normalized_new_status_code NVARCHAR(30) = UPPER(LTRIM(RTRIM(@new_status_code)));
    DECLARE @normalized_reason NVARCHAR(500) = NULLIF(LTRIM(RTRIM(@reason)), N'');

    DECLARE @audit_entity_id NVARCHAR(100);
    DECLARE @detail_json NVARCHAR(MAX);

    BEGIN TRY
        IF @@TRANCOUNT = 0
        BEGIN
            SET @started_tran = 1;
            BEGIN TRAN;
        END;

        SET @lock_resource = N'auth_user_status_change_' + CONVERT(NVARCHAR(36), @target_user_id);

        EXEC @lock_result = sys.sp_getapplock
            @Resource = @lock_resource,
            @LockMode = 'Exclusive',
            @LockOwner = 'Transaction',
            @LockTimeout = 10000;

        IF @lock_result < 0
        BEGIN
            ;THROW 53101, 'Could not acquire user status change lock.', 1;
        END;

        IF NOT EXISTS
        (
            SELECT 1
            FROM
            (
                SELECT
                    u.user_id,
                    u.role_id
                FROM auth.Users u
                WHERE u.user_id = @admin_user_id
                  AND u.status_code = N'ACTIVE'
            ) AS active_admin
            INNER JOIN auth.Roles r
                ON r.role_id = active_admin.role_id
            WHERE r.role_name = N'Admin'
        )
        BEGIN
            ;THROW 53102, 'Admin user is not active or does not have permission.', 1;
        END;

        SELECT
            @old_status_code = u.status_code
        FROM auth.Users u WITH (UPDLOCK, HOLDLOCK)
        WHERE u.user_id = @target_user_id;

        IF @old_status_code IS NULL
        BEGIN
            ;THROW 53103, 'Target user does not exist.', 1;
        END;

        IF @old_status_code = @normalized_new_status_code
        BEGIN
            IF @started_tran = 1
            BEGIN
                COMMIT;
            END;

            RETURN;
        END;

        UPDATE auth.Users
        SET
            status_code = @normalized_new_status_code
        WHERE user_id = @target_user_id;

        SELECT @detail_json =
        (
            SELECT
                @target_user_id AS user_id,
                @old_status_code AS old_status_code,
                @normalized_new_status_code AS new_status_code,
                @normalized_reason AS reason
            FOR JSON PATH, WITHOUT_ARRAY_WRAPPER
        );

       SET @audit_entity_id = CONVERT(NVARCHAR(36), @target_user_id);

EXEC audit.usp_AuditEvent_Append
    @actor_user_id = @admin_user_id,
    @action_code = N'USER_STATUS_CHANGED',
    @entity_type = N'Users',
    @entity_id = @audit_entity_id,
    @detail_json = @detail_json;

        IF @started_tran = 1
        BEGIN
            COMMIT;
        END;
    END TRY
    BEGIN CATCH
        IF @started_tran = 1 AND XACT_STATE() <> 0
        BEGIN
            ROLLBACK;
        END;

        ;THROW;
    END CATCH
END;
GO
CREATE OR ALTER PROCEDURE auth.usp_User_ResetPassword
    @target_user_id      UNIQUEIDENTIFIER,
    @new_password_hash   NVARCHAR(255),
    @actor_user_id       UNIQUEIDENTIFIER = NULL,
    @reset_mode          NVARCHAR(30),
    @reset_reason        NVARCHAR(500) = NULL
AS
BEGIN
    SET NOCOUNT ON;
    SET XACT_ABORT ON;

    DECLARE @started_tran BIT = 0;
    DECLARE @lock_result INT;
    DECLARE @lock_resource NVARCHAR(255);

    DECLARE @old_status_code NVARCHAR(30);
    DECLARE @normalized_reset_mode NVARCHAR(30) = UPPER(LTRIM(RTRIM(@reset_mode)));
    DECLARE @normalized_reset_reason NVARCHAR(500) = NULLIF(LTRIM(RTRIM(@reset_reason)), N'');

    DECLARE @detail_json NVARCHAR(MAX);
    DECLARE @action_code NVARCHAR(64);

    BEGIN TRY
        IF @normalized_reset_mode NOT IN (N'SELF_CHANGE', N'TOKEN_RESET', N'ADMIN_RESET')
        BEGIN
            ;THROW 52001, 'Invalid reset_mode.', 1;
        END;

        IF @new_password_hash IS NULL OR LEN(LTRIM(RTRIM(@new_password_hash))) < 20
        BEGIN
            ;THROW 52002, 'new_password_hash is invalid.', 1;
        END;

        IF @normalized_reset_mode = N'SELF_CHANGE'
           AND (@actor_user_id IS NULL OR @actor_user_id <> @target_user_id)
        BEGIN
            ;THROW 52004, 'Self password change must be performed by the target user.', 1;
        END;

        IF @normalized_reset_mode = N'ADMIN_RESET'
           AND @actor_user_id IS NULL
        BEGIN
            ;THROW 52005, 'Admin password reset requires an actor user.', 1;
        END;

        IF @@TRANCOUNT = 0
        BEGIN
            SET @started_tran = 1;
            BEGIN TRAN;
        END;

        /*
            Serialize password reset/change for the same target user.
            GUID values should be converted with NVARCHAR(36).
        */
        SET @lock_resource = N'auth_user_password_reset_' + CONVERT(NVARCHAR(36), @target_user_id);

        EXEC @lock_result = sys.sp_getapplock
            @Resource = @lock_resource,
            @LockMode = 'Exclusive',
            @LockOwner = 'Transaction',
            @LockTimeout = 10000;

        IF @lock_result < 0
        BEGIN
            ;THROW 52006, 'Could not acquire password reset lock.', 1;
        END;

        /*
            Admin reset must be performed by an ACTIVE Admin.
        */
        IF @normalized_reset_mode = N'ADMIN_RESET'
        BEGIN
            IF NOT EXISTS
            (
                SELECT 1
                FROM
                (
                    SELECT
                        u.user_id,
                        u.role_id
                    FROM auth.Users u
                    WHERE u.user_id = @actor_user_id
                      AND u.status_code = N'ACTIVE'
                ) AS active_actor
                INNER JOIN auth.Roles r
                    ON r.role_id = active_actor.role_id
                WHERE r.role_name = N'Admin'
            )
            BEGIN
                ;THROW 52007, 'Actor user is not an active Admin.', 1;
            END;
        END;

        /*
            Read target user with update lock.
        */
        SELECT
            @old_status_code = status_code
        FROM auth.Users WITH (UPDLOCK, HOLDLOCK)
        WHERE user_id = @target_user_id;

        IF @old_status_code IS NULL
        BEGIN
            ;THROW 52003, 'Target user does not exist.', 1;
        END;

        /*
            Password reset updates only password_hash.

            It must not approve, reject, disable, or re-enable accounts.
            Therefore:
            - PENDING_APPROVAL stays PENDING_APPROVAL
            - ACTIVE stays ACTIVE
            - REJECTED stays REJECTED
            - DISABLED stays DISABLED
        */
        UPDATE auth.Users
        SET
            password_hash = @new_password_hash
        WHERE user_id = @target_user_id;

        SET @action_code =
            CASE
                WHEN @normalized_reset_mode = N'SELF_CHANGE' THEN N'PASSWORD_CHANGED'
                WHEN @normalized_reset_mode = N'TOKEN_RESET' THEN N'PASSWORD_RESET_BY_TOKEN'
                WHEN @normalized_reset_mode = N'ADMIN_RESET' THEN N'PASSWORD_RESET_BY_ADMIN'
            END;

        SELECT @detail_json =
        (
            SELECT
                @target_user_id AS user_id,
                @old_status_code AS status_code,
                @normalized_reset_mode AS reset_mode,
                @normalized_reset_reason AS reset_reason,
                N'Password hash updated.' AS result
            FOR JSON PATH, WITHOUT_ARRAY_WRAPPER
        );

        DECLARE @audit_entity_id NVARCHAR(100);

SET @audit_entity_id = CONVERT(NVARCHAR(36), @target_user_id);

EXEC audit.usp_AuditEvent_Append
    @actor_user_id = @actor_user_id,
    @action_code = @action_code,
    @entity_type = N'Users',
    @entity_id = @audit_entity_id,
    @detail_json = @detail_json;

        IF @started_tran = 1
        BEGIN
            COMMIT;
        END;
    END TRY
    BEGIN CATCH
        IF @started_tran = 1 AND XACT_STATE() <> 0
        BEGIN
            ROLLBACK;
        END;

        ;THROW;
    END CATCH
END;
GO
