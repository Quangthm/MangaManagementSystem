    SET XACT_ABORT ON;

    DECLARE @started_tran BIT = 0;
    DECLARE @has_portfolio BIT = 0;

    BEGIN TRY
        SET @portfolio_file_resource_id = NULL;

        IF @portfolio_cloudinary_public_id IS NOT NULL
        BEGIN
            SET @has_portfolio = 1;
        END;

        IF @@TRANCOUNT = 0
        BEGIN
            SET @started_tran = 1;
            BEGIN TRAN;
        END;

        /*
            Create the user first.
            User ID is generated by auth.Users DEFAULT NEWID()
            and returned through auth.usp_User_Create.
        */
        EXEC auth.usp_User_Create
            @role_name = @role_name,
